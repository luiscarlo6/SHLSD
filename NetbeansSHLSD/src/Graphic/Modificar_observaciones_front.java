    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        String carnet =f_carne.getText().toString();
        String fecha = f_fecha.getText().toString();
        Hora inicio = new Hora(f_inicio.getText().toString().trim());

        Boolean validos = false;
        while(!validos) {
            if (Hora.validarFormato(inicio.getCadena())){
                inicio = new Hora(f_inicio.getText().toString());
            } else {
                Notificacion notificacion = new Notificacion(this,true, "Ingrese una hora en formato militar");
                notificacion.setVisible(true);
            }
            
            if (!Hora.validarFormatoFecha(fecha)){
           
                Notificacion notificacion = new Notificacion(this,true, "Ingrese la fecha en el formato indicado");
                notificacion.setVisible(true);
            }
            
            if (B_A_estudiante.validaCarnet(carnet)){
                if (null == B_A_estudiante.consultarEstudiante(carnet)){
                     Notificacion notificacion = new Notificacion(this,true, "Ese estudiante no existe");
                     notificacion.setVisible(true);
                }
            } else {
                Notificacion notificacion = new Notificacion(this,true, "Ingrese un carnet valido");
                notificacion.setVisible(true);
            }
            
            B_A_horas labor = B_A_horas.consultarLabor(carnet.trim(), fecha.trim(), inicio);
        
            if (null != labor) {
                validos = true;  
                Modificar_observaciones ventana = new Modificar_observaciones(labor);
                ventana.setVisible(true);            
                this.setVisible(false);
                break;
            } else {
                Notificacion notificacion = new Notificacion(this,true, "Esa labor no existe");
                notificacion.setVisible(true);
                break;
            }
        }
         
    }//GEN-LAST:event_modificarActionPerformed

